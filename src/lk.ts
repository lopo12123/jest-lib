function lk(nums: number[]): number {
    let count = 0
    let left = nums[0], check = nums[1]

    for (let i = 2; i < nums.length; i++) {
        if(nums[i] !== check) {
            if(check > left && check > nums[i]
                || check < left && check < nums[i]) count += 1
            left = check
            check = nums[i]
        }
    }

    return count
}

