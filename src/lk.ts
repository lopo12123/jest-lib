function lk(code: number[], k: number): number[] {
    const n = code.length
    const abs_k = Math.abs(k)
    const flag = k > 0 ? 1 : -1
    const decoded: number[] = []

    if (k === 0)
        return new Array(code.length).fill(0)
    else {
        for (let i = 0; i < n; i++) {
            let _sum = 0
            for (let j = 1; j <= abs_k; j++) {
                _sum += code.at((i + flag * j) % n)!
            }
            decoded[i] = _sum
        }
        return decoded
    }
}

