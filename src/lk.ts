function lk(nums: number[]): number {
    if(nums.length === 1) return 1

    let sum = 1
    let l = 0

    nums.push(nums[nums.length - 1] - 1)
    for(let i = 1; i < nums.length; i ++) {
        if(nums[i] <= nums[i - 1]) {
            sum = Math.max(sum, i - l)
            l = i
        }
    }

    return sum
}

